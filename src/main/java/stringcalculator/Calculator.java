package stringcalculator;

public class Calculator {

    public Calculator() {
    }

    public int add(int i, int j) {
        return i + j;
    }

    public int subtract(int i, int j) {
        return i - j;
    }

    public int multiplay(int i, int j) {
        return i * j;
    }

    public int divide(int i, int j) {
        return i / j;
    }

}
